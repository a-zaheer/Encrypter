void shiftRows(uint8_t bytes[])
{
    uint8_t row[WORD_SIZE];
    for (size_t i = 1; i < WORD_SIZE; i++)
    {
        copy(row, 0, bytes, (WORD_SIZE * i), WORD_SIZE); // copy row to be shifted
        rotateLeft(row, WORD_SIZE, i);
        copy(bytes, (WORD_SIZE * i), row, 0, WORD_SIZE); // copy back shifted row
    }
}

void invShiftRows(uint8_t bytes[])
{
    uint8_t row[WORD_SIZE];
    for (size_t i = 1; i < WORD_SIZE; i++)
    {
        copy(row, 0, bytes, (WORD_SIZE * i), WORD_SIZE); // copy row to be shifted
        rotateLeft(row, WORD_SIZE, WORD_SIZE - i);
        copy(bytes, (WORD_SIZE * i), row, 0, WORD_SIZE); // copy back shifted row
    }
}

void mixColumns(uint8_t bytes[], Direction direction)
{
    uint8_t *column;
    uint8_t rows[WORD_SIZE][WORD_SIZE];

    static uint8_t MBOX[] =
        {0x02, 0x03, 0x01, 0x01,
         0x01, 0x02, 0x03, 0x01,
         0x01, 0x01, 0x02, 0x03,
         0x03, 0x01, 0x01, 0x02};
    static uint8_t INVMBOX[] =
        {0x0E, 0x0B, 0x0D, 0x09,
         0x09, 0x0E, 0x0B, 0x0D,
         0x0D, 0x09, 0x0E, 0x0B,
         0x0B, 0x0D, 0x09, 0x0E};

    //matrix representaion of the linear transformation in the MixColumns step
    uint8_t *matrix = (direction == ENCRYPT) ? MBOX : INVMBOX;

    for (size_t i = 0; i < WORD_SIZE; i++)
    {
        copy(rows[i], 0, matrix, i * WORD_SIZE, WORD_SIZE);
    }

    //standard matrix multiplication
    for (size_t j = 0; j < WORD_SIZE; j++)
    {
        column = slice(bytes, j, j + ((WORD_SIZE - 1) * WORD_SIZE), WORD_SIZE);
        for (size_t i = 0; i < WORD_SIZE; i++)
        {
            bytes[(i * WORD_SIZE) + j] = dot(rows[i], column, WORD_SIZE);
        }
        free(column); 
    }
}

void keyExpansion(uint8_t initialkey[], uint8_t * expandedKey, int rounds)
{
    size_t len = KEY_SIZE;
    // fill in initial key
    copy(expandedKey, 0, initialkey, 0, len);

    // fill in expansion
    uint8_t *lastFour;
    uint8_t *firstFour;
    for (int round = 0; round < rounds; round++)
    {
        for (int j = 0; j < KEY_STEP; j++)
        {
            // grab the last four bytes of key expansion
            lastFour = slice(expandedKey, len - KEY_STEP, len, 1);
            if (j == 0)
                expansionCore(lastFour, round);

            // grab the first four bytes starting at the last KEY_SIZE blocks of the key expansion
            firstFour = slice(expandedKey, len - KEY_SIZE, len - KEY_SIZE + KEY_STEP, 1);
            lastFour = arrXor(lastFour, firstFour, KEY_STEP);

            // append new four bytes to key expansion
            copy(expandedKey, len, lastFour, 0, KEY_STEP);
            len += KEY_STEP;

            free(lastFour);
            free(firstFour);
        }
    }
}

uint8_t *expansionCore(uint8_t bytes[], int roundNum)
{
    uint8_t roundConstant = 0x01; // x^0 polynomial

    rotateLeft(bytes, WORD_SIZE, 1);

    for (size_t i = 0; i < WORD_SIZE; i++)
    {
        bytes[i] = subByte(bytes[i], ENCRYPT);
    }

    // build up round constant
    for (int i = 0; i < roundNum; i++)
    {
        roundConstant = xtime(roundConstant);
    }
    bytes[0] = bytes[0] ^ roundConstant; // only xor to the first byte

    return bytes;
}

/** 
 * A valid PKCS7 padding has a minimum of 1 pad byte at the end indicating 'n' total number of padding 
 * bytes. All the pad bytes must have the same value. Validity is checked by comparing the last 'n' bytes. 
*/
uint8_t checkPad(uint8_t block[])
{
    uint8_t pad = block[BLOCK_SIZE - 1];
    if (pad >= BLOCK_SIZE)
        return 0;

    uint8_t padBytes = pad ? pad : BLOCK_SIZE; /* zero padding = 16 bytes*/
    for (size_t i = BLOCK_SIZE - 1; i >= (BLOCK_SIZE - padBytes); i--)
    {
        if (block[i] != pad)
            return 0;
    }
    return padBytes;
}

void pad(uint8_t block[], size_t len)
{
    if (len >= BLOCK_SIZE)
        return; 
    size_t pad = (BLOCK_SIZE - len) % BLOCK_SIZE;
    for (size_t i = len; i < BLOCK_SIZE; i++)
    {
        block[i] = (uint8_t)pad;
    }
}